class Calculator {
    add(num1, num2) {
        return num1 + num2; // Addition operation
    }

    subtract(num1, num2) {
        return num1 - num2; // Subtraction operation
    }

    divide(num1, num2) {
        return num1 / num2; // Division operation
    }

    multiply(num1, num2) {
        return num1 * num2; // Multiplication operation
    }
}
